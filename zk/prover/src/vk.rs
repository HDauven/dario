fn fq(dec: &str) -> Result<Fq> {
    let n: BigUint = dec.parse().map_err(|e| anyhow!("bad decimal: {e}"))?;
    Ok(Fq::from_le_bytes_mod_order(&n.to_bytes_le()))
}

fn g1(x: &str, y: &str) -> Result<G1Affine> {
    Ok(G1Affine::new(fq(x)?, fq(y)?))
}

fn g2(x1: &str, x2: &str, y1: &str, y2: &str) -> Result<G2Affine> {
    // Solidity constants order: X1 = c1, X2 = c0 (imaginary first).
    Ok(G2Affine::new(
        Fq2::new(fq(x2)?, fq(x1)?),
        Fq2::new(fq(y2)?, fq(y1)?),
    ))
}

